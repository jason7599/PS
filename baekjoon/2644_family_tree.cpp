const pii DIRS[4] = {{1, 0}, {0, 1}, {-1, 0}, {0, -1}}; // drul

int parents[101];
int dist[101];

int main() {
    cin.tie(0)->sync_with_stdio(0);
    
    int n_nodes = input();
    auto [a, b] = inputs<int, 2>();

    REP(input()) {
        auto [i, j] = inputs<int, 2>();
        parents[j] = i;
    }
    
    fill(&dist[1], &dist[1 + n_nodes], -1);
    for (int n = a, d = 0; n; n = parents[n], d++) {
        dist[n] = d;
    }

    int ans = -1;
    for (int n = b, d = 0; n; n = parents[n], d++) {
        if (dist[n] != -1) {
            ans = dist[n] + d;
            break;
        }
    }

    print(ans);
}