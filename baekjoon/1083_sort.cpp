const pii DIRS[4] = {{1, 0}, {0, 1}, {-1, 0}, {0, -1}};

int n, arr[50];

int main() {
    cin.tie(0)->sync_with_stdio(0);

    FOR(i, input(n)) {
        input(arr[i]);
    }
    int s = input();

    FOR(i, n) {
        int max_i = i;
        for (int j = i + 1; j < min(n, i + s + 1); j++) {
            if (arr[max_i] < arr[j]) {
                max_i = j;
            }
        }
        cout << arr[max_i] << ' ';

        s -= max_i - i;
        for (int j = max_i; j > i; j--) {
            arr[j] = arr[j - 1];
        }
    }
    LF;
}